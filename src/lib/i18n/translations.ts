export function getTranslations(lang: LangCode) {
  return translations[lang] ?? translations.es
}

export function translate(lang: LangCode, key: TranslationKey, fallback?: string): string {
  const dict = getTranslations(lang)
  return (dict as Record<string, string>)[key] ?? (translations.es as Record<string, string>)[key] ?? fallback ?? key
}
